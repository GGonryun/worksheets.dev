export const parseMonsterId = (id: unknown): MobId => {
  const monsterId = Number(id);
  if (MOBS.some((mob) => mob.id === monsterId)) {
    return monsterId as MobId;
  } else {
    throw new Error(`Invalid monster id ${id}`);
  }
};
